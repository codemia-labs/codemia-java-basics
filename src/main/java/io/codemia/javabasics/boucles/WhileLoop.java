package io.codemia.javabasics.boucles;

public class WhileLoop {

    public static void run() {

        System.out.println("=== Boucle while ===");

        int counter = 1;

        while (counter <= 5) {
            System.out.println("Compteur : " + counter);
            counter++;
        }

        System.out.println();

    }

}