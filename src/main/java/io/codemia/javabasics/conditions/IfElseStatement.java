package io.codemia.javabasics.conditions;

public class IfElseStatement {

    public static void run() {

        System.out.println("=== Condition if / else ===");

        int age = 15;

        if (age >= 18) {
            System.out.println("Majeur");
        } else {
            System.out.println("Mineur");
        }

        System.out.println();

    }

}